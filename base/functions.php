  return $num1 + $num2;
}

function resta(int $num1, int $num2) {
  return $num1 - $num2;
}

function multiplica(int $num1, int $num2) {
  return $num1 * $num2;
}

function divide(int $num1, int $num2) {
  return $num1 / $num2;
